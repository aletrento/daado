 <h3 align="center"><img src="img/what.png" style="vertical-align:middle" width="45" height="45" alt="iscriviti">&nbsp;&nbsp;&nbsp;&nbsp;<font color="#E68A00">Cos'è DAADO?</font></h3>

<br>

<div>
<span class="firstchar">Q</span>uesto sito è rivolto a tutti i docenti delle scuole secondarie di secondo grado del Trentino.<br>
DAADO è stato pensato per facilitare la registrazione di tutte le attività che si svolgono in ambito scolastico (oltre alle ore di cattedra). L'inserimento dei dati è <a href="gradimento.php">veloce ed intuitivo</a>, e il sistema calcola automaticamente la durata di ciascun evento, in unità orarie di 50' o 60'.
<br><br>
I <a href="flusso.php">dati salvati</a> vengono riportati in una tabella che può essere modificata ed esportata in formato .csv (OpenOffice Calc). Il consuntivo viene compilato in modo automatico e si può esportare in formato .odt (OpenOffice Writer).
<br><br>
Le attività registrate dai docenti possono essere visualizzate e stampate dal personale di segreteria, per un monitoraggio immediato senza moduli da compilare.<br>
Per saperne di più, scarica la <a href="docs/guida_DAADO.pdf">guida</a> o guarda un <a href="docs/video_daado.webm">breve video</a> (12.5 MB).
